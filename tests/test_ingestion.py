class TestRegistry:
    """SourceRegistry must auto-register FileSource and handle lookups."""

    def test_file_source_registered(self) -> None:
        """SourceRegistry must contain a 'file' entry by default."""
        from ingestion.registry import SourceRegistry
        registry = SourceRegistry()
        source = registry.get("file")
        assert source is not None
        assert source.name == "file"

    def test_get_unknown_returns_none(self) -> None:
        """get() on an unknown name must return None."""
        from ingestion.registry import SourceRegistry
        registry = SourceRegistry()
        assert registry.get("nope") is None

    def test_list_sources_non_empty(self) -> None:
        """list_sources() must return at least one entry."""
        from ingestion.registry import SourceRegistry
        registry = SourceRegistry()
        sources = registry.list_sources()
        assert isinstance(sources, list)
        assert len(sources) >= 1

    def test_list_sources_contains_file(self) -> None:
        """list_sources() must include the 'file' source metadata."""
        from ingestion.registry import SourceRegistry
        registry = SourceRegistry()
        names = [s["name"] for s in registry.list_sources()]
        assert "file" in names

    def test_check_all_connections_file_true(self) -> None:
        """check_all_connections() must report file source as True."""
        from ingestion.registry import SourceRegistry
        registry = SourceRegistry()
        results = registry.check_all_connections()
        assert results.get("file") is True

    def test_register_custom_source(self) -> None:
        """A manually registered source must be retrievable by name."""
        from ingestion.registry import SourceRegistry
        from ingestion.base_source import BaseSource

        class _DummySource(BaseSource):
            source_type = "api"

            def connect(self) -> bool:
                return True

            def extract(self, **kwargs) -> pd.DataFrame:
                return pd.DataFrame()

        registry = SourceRegistry(auto_register=False)
        dummy = _DummySource(name="dummy", description="Test only")
        registry.register(dummy)
        assert registry.get("dummy") is dummy

    def test_empty_registry_list(self) -> None:
        """An empty registry (auto_register=False) must return an empty list."""
        from ingestion.registry import SourceRegistry
        registry = SourceRegistry(auto_register=False)
        assert registry.list_sources() == []
