# ---------------------------------------------------------------------------
# Error / edge-case handling
# ---------------------------------------------------------------------------

class TestErrorHandling:
    """FileSource must never raise; bad inputs return empty DataFrames."""

    def test_missing_file_returns_empty_dataframe(self) -> None:
        """Non-existent file path must return an empty DataFrame, not raise."""
        df = _source().extract(filepath="/no/such/file/nowhere.csv")
        assert isinstance(df, pd.DataFrame)
        assert df.empty

    def test_unsupported_suffix_returns_empty_dataframe(
        self, sample_dir: Path
    ) -> None:
        """A file with an unsupported suffix (.xyz) must return empty DataFrame."""
        bad_path = sample_dir / "fake.xyz"
        bad_path.write_text("hello")
        try:
            df = _source().extract(filepath=str(bad_path))
            assert isinstance(df, pd.DataFrame)
            assert df.empty
        finally:
            bad_path.unlink(missing_ok=True)


# ---------------------------------------------------------------------------
# Metadata bookkeeping
# ---------------------------------------------------------------------------

class TestMetadataBookkeeping:
    """last_extract_time and last_record_count must be updated after extract."""

    def test_record_count_updated_after_extract(self, fixture_csv: Path) -> None:
        """last_record_count must equal row count after a successful extract."""
        src = _source()
        assert src.last_record_count == -1  # initial sentinel
        src.extract(filepath=str(fixture_csv))
        assert src.last_record_count == EXPECTED_ROW_COUNT

    def test_extract_time_set_after_extract(self, fixture_csv: Path) -> None:
        """last_extract_time must be populated after a successful extract."""
        src = _source()
        assert src.last_extract_time is None
        src.extract(filepath=str(fixture_csv))
        assert src.last_extract_time is not None

    def test_get_metadata_keys(self, fixture_csv: Path) -> None:
        """get_metadata() must return all required keys."""
        src = _source()
        src.extract(filepath=str(fixture_csv))
        meta = src.get_metadata()
        required = {
            "name", "description", "source_type",
            "last_extract_time", "last_record_count",
        }
        assert required.issubset(meta.keys())
        assert meta["source_type"] == "file"
        assert meta["last_record_count"] == EXPECTED_ROW_COUNT


