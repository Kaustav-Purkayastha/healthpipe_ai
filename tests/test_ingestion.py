"""
tests/test_ingestion.py — Unit tests for ingestion/base_source, file_source, registry.

All tests are hermetic: no network calls, no real database.
Fixture files are generated by conftest.py if absent.
"""

from __future__ import annotations

import tempfile
from pathlib import Path

import pandas as pd
import pytest

from ingestion.file_source import FileSource

# Expected columns produced by make_fixtures.build_fixtures()
EXPECTED_COLUMNS = {
    "patient_id", "age", "state", "diagnosis", "visit_date", "cost_usd"
}
EXPECTED_ROW_COUNT = 20


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _source() -> FileSource:
    """Return a fresh FileSource instance for each test."""
    return FileSource()


# ---------------------------------------------------------------------------
# CSV
# ---------------------------------------------------------------------------

class TestCSVLoading:
    """Tests for CSV file loading via FileSource."""

    def test_csv_row_count(self, fixture_csv: Path) -> None:
        """CSV fixture must load exactly 20 rows."""
        df = _source().extract(filepath=str(fixture_csv))
        assert len(df) == EXPECTED_ROW_COUNT

    def test_csv_columns(self, fixture_csv: Path) -> None:
        """CSV fixture must have the expected column set."""
        df = _source().extract(filepath=str(fixture_csv))
        assert EXPECTED_COLUMNS == set(df.columns)

    def test_csv_chunk_size(self, fixture_csv: Path) -> None:
        """Chunked CSV read must produce the same 20 rows as a direct read."""
        df = _source().extract(filepath=str(fixture_csv), chunk_size=7)
        assert len(df) == EXPECTED_ROW_COUNT
        assert EXPECTED_COLUMNS == set(df.columns)

    def test_csv_encoding_fallback_latin1(self) -> None:
        """A latin-1 file containing é must load successfully via fallback chain."""
        content = "name,note\nJosé,café\nAna,résumé\n"
        with tempfile.NamedTemporaryFile(
            suffix=".csv", mode="wb", delete=False
        ) as tmp:
            tmp.write(content.encode("latin-1"))
            tmp_path = Path(tmp.name)

        try:
            df = _source().extract(filepath=str(tmp_path))
            assert len(df) == 2
            assert "José" in df["name"].values
        finally:
            tmp_path.unlink(missing_ok=True)


